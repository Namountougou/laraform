Avec laravel on peut creer des pages web avec des templates.
laravel fonctionne avec des templates.
pour avoir un affichage laravel on utilise la methode view() dans  le controlleur qui se trouve dans le dossier app\Http\Controllers.
ensuite on utilise la methode make() dans le controlleur pour creer une vue qui se trouve dans le dossier resources\views.
pour avoir acces a la vue on utilise la methode view() dans le controlleur .
la route permet de creer des liens entre les pages web et les controlleurs et les templates .
les routes sont definies dans le dossier routes\web.php.


